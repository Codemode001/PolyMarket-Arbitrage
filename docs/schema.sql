CREATE TABLE public.agent_config (
  id integer NOT NULL DEFAULT 1 CHECK (id = 1),
  is_running boolean DEFAULT false,
  mode text DEFAULT 'semi-auto'::text,
  max_bet_per_market numeric DEFAULT 10,
  daily_budget numeric DEFAULT 50,
  daily_spent numeric DEFAULT 0,
  min_edge_threshold numeric DEFAULT 0.15,
  min_confidence text DEFAULT 'medium'::text,
  budget_reset_at timestamp with time zone DEFAULT now(),
  updated_at timestamp with time zone DEFAULT now(),
  arb_min_profit_pct numeric DEFAULT 0.02,
  arb_max_per_trade numeric DEFAULT 50,
  scan_interval_seconds integer DEFAULT 30,
  arb_mode text DEFAULT 'yes_no'::text,
  CONSTRAINT agent_config_pkey PRIMARY KEY (id)
);
CREATE TABLE public.agent_logs (
  id uuid NOT NULL DEFAULT gen_random_uuid(),
  event text NOT NULL,
  payload jsonb,
  level text DEFAULT 'info'::text,
  created_at timestamp with time zone DEFAULT now(),
  message text,
  CONSTRAINT agent_logs_pkey PRIMARY KEY (id)
);
CREATE TABLE public.arb_executions (
  id uuid NOT NULL DEFAULT gen_random_uuid(),
  opportunity_id uuid,
  bet_a_id uuid,
  bet_b_id uuid,
  total_staked numeric NOT NULL DEFAULT 0,
  expected_profit numeric NOT NULL DEFAULT 0,
  actual_profit numeric,
  status text DEFAULT 'open'::text,
  created_at timestamp with time zone DEFAULT now(),
  resolved_at timestamp with time zone,
  bet_id_a uuid,
  bet_id_b uuid,
  size_usdc numeric,
  expected_profit_usdc numeric,
  market_id text,
  CONSTRAINT arb_executions_pkey PRIMARY KEY (id),
  CONSTRAINT arb_executions_opportunity_id_fkey FOREIGN KEY (opportunity_id) REFERENCES public.arb_opportunities(id),
  CONSTRAINT arb_executions_bet_a_id_fkey FOREIGN KEY (bet_a_id) REFERENCES public.bets(id),
  CONSTRAINT arb_executions_bet_b_id_fkey FOREIGN KEY (bet_b_id) REFERENCES public.bets(id),
  CONSTRAINT arb_executions_bet_id_a_fkey FOREIGN KEY (bet_id_a) REFERENCES public.bets(id),
  CONSTRAINT arb_executions_bet_id_b_fkey FOREIGN KEY (bet_id_b) REFERENCES public.bets(id)
);
CREATE TABLE public.arb_opportunities (
  id uuid NOT NULL DEFAULT gen_random_uuid(),
  type text NOT NULL,
