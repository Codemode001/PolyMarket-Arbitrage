  market_id_b text,
  token_id_a text,
  token_id_b text,
  price_a numeric NOT NULL,
  price_b numeric,
  combined_price numeric,
  expected_profit_pct numeric NOT NULL,
  status text DEFAULT 'found'::text,
  found_at timestamp with time zone DEFAULT now(),
  executed_at timestamp with time zone,
  notes text,
  created_at timestamp with time zone DEFAULT now(),
  market_id text,
  CONSTRAINT arb_opportunities_pkey PRIMARY KEY (id),
  CONSTRAINT arb_opportunities_market_id_a_fkey FOREIGN KEY (market_id_a) REFERENCES public.markets(id),
  CONSTRAINT arb_opportunities_market_id_b_fkey FOREIGN KEY (market_id_b) REFERENCES public.markets(id)
);
